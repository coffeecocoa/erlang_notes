-module(notes_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	application:start(mnesia),
    	notes_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
	application:stop(mnesia),
    ok.

